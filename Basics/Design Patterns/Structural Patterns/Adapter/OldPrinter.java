public class OldPrinter {
    public void printData(String data) {
        System.out.println("Old Printer: Printing data - " + data);
    }

    public void loadPaper(int pages) {
        System.out.println("Old Printer: Loading " + pages + " papers.");
    }

    public void turnOff() {
        System.out.println("Old Printer: Turning off.");
    }
}
